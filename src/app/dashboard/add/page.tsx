import { auth } from '@/nextAuth/auth';
import { redirect } from 'next/navigation';
import MainContent from './MainContent';


///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////


const page = async () => {


    const session = await auth();
    if (!session?.user) {
        redirect("/auth/login")
    }

  
  ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////


  return (
    <>
     <MainContent/> 
    </>
  )
}

export default page
