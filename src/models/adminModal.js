import mongoose from 'mongoose';

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////

const adminSchema = new mongoose.Schema({
    email: {
        type: String,
        required: true,
    },
    name: {
        type: String,
        required: true,
    },
    phoneNumber: {
        type: String,
        required: true,
    },
    password: {
        type: String,
        required: true,
    },
})


///////////////////////////////////////////////////////////////////////////////////////////////////////////////////


const Admin = mongoose.models?.admin || mongoose.model("admin", adminSchema);


///////////////////////////////////////////////////////////////////////////////////////////////////////////////////

export default Admin;